if run_btn:
    from datasets import Dataset
    from ragas import evaluate, RunConfig
    from ragas.metrics import faithfulness, answer_relevancy, context_precision

    # Switch to correct chunk size index
    if indexed_paths:
        with st.spinner(f"Switching index to chunk size {chunk_size}..."):
            rag.build_index(indexed_paths, chunk_size=chunk_size)

    # Load answers + re-retrieve context from FAISS (free — no API call)
    prog = st.progress(0, text="Loading answers and retrieving context...")
    questions, answers, contexts, ground_truths = [], [], [], []

    for i, q in enumerate(QUESTIONS):
        prog.progress((i + 1) / 10, text=f"Preparing Q{q['id']}/10...")
        row = df_answers[df_answers["question_id"] == q["id"]]
        if row.empty:
            continue
        answer = row.iloc[0]["answer"]
        chunks = rag.search(q["text"], top_k=top_k)
        ctx    = [c["text"] for c in chunks] if chunks else ["No context retrieved."]
        questions.append(q["text"])
        answers.append(answer)
        contexts.append(ctx)
        ground_truths.append("")

    prog.empty()

    if not questions:
        st.error("No valid answers found. Check the database has questions for this experiment.")
        st.stop()

    st.info(
        f"Prepared {len(questions)} question-answer-context triples. "
        f"Running RAGAS judge — ~{len(questions) * 12} calls on {JUDGE_MODEL}. "
        f"Takes 5–15 minutes depending on Groq response times."
    )

    try:
        dataset = Dataset.from_dict({
            "question":     questions,
            "answer":       answers,
            "contexts":     contexts,
            "ground_truth": ground_truths,
        })

        # Explicitly bind LLM and embeddings to each metric.
        # This is essential — without it, RAGAS async worker threads
        # lose the LLM reference and throw AssertionError: LLM is not set.
        faithfulness.llm           = JUDGE_LLM
        answer_relevancy.llm       = JUDGE_LLM
        answer_relevancy.embeddings = JUDGE_EMBS
        context_precision.llm      = JUDGE_LLM

        result = evaluate(
            dataset,
            metrics=[faithfulness, answer_relevancy, context_precision],
            llm=JUDGE_LLM,
            embeddings=JUDGE_EMBS,
            run_config=RunConfig(
                max_workers=1,   # sequential — prevents async LLM binding loss
                timeout=600,     # 10 min per job — handles slow judge calls
                max_retries=5,   # retries on Failed to parse / timeout
            ),
        )

        df_r = result.to_pandas()

        def safe_mean(col):
            if col not in df_r.columns:
                return 0.0
            v = float(df_r[col].mean())
            return 0.0 if math.isnan(v) else round(v, 4)

        faith   = safe_mean("faithfulness")
        relev   = safe_mean("answer_relevancy")
        prec    = safe_mean("context_precision")
        overall = round((faith + relev + prec) / 3, 4)

        if faith == 0.0 and relev == 0.0 and prec == 0.0:
            st.error(
                "All metrics came back as 0 or NaN — every judge call failed. "
                "This usually means the Groq API key is exhausted or invalid. "
                "Check console.groq.com/settings/limits and try again in a few minutes."
            )
            st.stop()

        save_result(exp_id, chunk_size, top_k, faith, relev, prec, overall, len(questions))
        st.session_state["ragas_latest"] = {
            "experiment":        exp_id,
            "chunk_size":        chunk_size,
            "top_k":             top_k,
            "faithfulness":      faith,
            "answer_relevancy":  relev,
            "context_precision": prec,
            "overall":           overall,
            "n":                 len(questions),
        }
        st.rerun()

    except Exception as e:
        err = str(e)
        st.error(f"RAGAS scoring failed: {err}")
        if "rate_limit" in err.lower() or "429" in err:
            st.info(
                "Groq rate limit hit during judging. "
                "The 2-second delay should prevent this — wait 1 minute and try again."
            )
        elif "openai" in err.lower() or "api_key" in err.lower():
            st.info(
                "RAGAS fell back to OpenAI — the Groq judge is not bound correctly. "
                "Check GROQ_API_KEY is set in .streamlit/secrets.toml."
            )


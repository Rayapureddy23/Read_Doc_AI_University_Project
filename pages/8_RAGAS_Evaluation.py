def init_db():
    os.makedirs(os.path.dirname(DB), exist_ok=True)
    conn = sqlite3.connect(DB)
    conn.execute("""
        CREATE TABLE IF NOT EXISTS ragas_results (
            id                INTEGER PRIMARY KEY AUTOINCREMENT,
            experiment        TEXT UNIQUE,
            chunk_size        INTEGER,
            top_k             INTEGER,
            faithfulness      REAL,
            answer_relevancy  REAL,
            context_precision REAL,
            overall           REAL,
            num_questions     INTEGER,
            created_at        TEXT
        )""")
    conn.commit()
    conn.close()

def load_answers(exp):
    if not os.path.exists(DB):
        return pd.DataFrame()
    conn = sqlite3.connect(DB)
    try:
        df = pd.read_sql_query(
            "SELECT * FROM experiment_scores WHERE experiment=? ORDER BY question_id",
            conn, params=(exp,))
    except Exception:
        df = pd.DataFrame()
    conn.close()
    return df

def save_result(exp, cs, k, faith, relev, prec, overall, n):
    conn = sqlite3.connect(DB)
    conn.execute("DELETE FROM ragas_results WHERE experiment=?", (exp,))
    conn.execute("""
        INSERT INTO ragas_results
            (experiment, chunk_size, top_k, faithfulness,
             answer_relevancy, context_precision, overall,
             num_questions, created_at)
        VALUES (?,?,?,?,?,?,?,?,datetime('now'))
    """, (exp, cs, k, faith, relev, prec, overall, n))
    conn.commit()
    conn.close()

def load_all_results():
    if not os.path.exists(DB):
        return pd.DataFrame()
    conn = sqlite3.connect(DB)
    try:
        df = pd.read_sql_query(
            "SELECT * FROM ragas_results ORDER BY experiment", conn)
    except Exception:
        df = pd.DataFrame()
    conn.close()
    return df

